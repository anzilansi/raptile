# Enhanced colors for better readability
terminal_colors = ["red", "green", "yellow", "blue", "magenta", "cyan", "white"]

# Function to animate typing effect with colors
def animate_banner(banner):
    lines = banner.split("\n")
    for i, line in enumerate(lines):
        color = terminal_colors[i % len(terminal_colors)]  # Cycle through colors
        print(colored(line, color, attrs=['bold']))
        time.sleep(0.05)  # Adjust speed of animation for smoother effect

# Function to play background music
def play_music():
    playsound("rap.mp3")  # Ensure 'background.mp3' exists in the same directory

# Main function
def main():
    # Play background music in a separate thread
    music_thread = threading.Thread(target=play_music, daemon=True)
    music_thread.start()

    # Animate banner with colors
    animate_banner(ascii_banner)

    # Let the music play for 7 seconds
    time.sleep(7)

if __name__ == "__main__":
    main()


